import Foundation


class Math {
    func sum(a: Int, b:Int) -> Int {
        return a + b
    }
    
    func mul(a: Int, b: Int) -> Int {
        return a * b
    }
    
    func div(a: Int, b: Int) -> Int {
        return a / b
    }
    
    func subs(a: Int, b: Int) -> Int {
        return a - b
    }
}
